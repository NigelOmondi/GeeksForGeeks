// Conditional statements in javascript

/*  if statement
    executing code based on a condition.
*/

/*  else statement
    Alternative code execution when ‘if’ condition is not met.
*/

/*  else if statement
    Additional condition check after ‘if’ and before ‘else’ statement.
*/

/*  switch statement
    Multi-case conditional statement for executing code 
    based on a variable’s value.
*/

const num = 0;

if (num > 0) {
    console.log("Given number is positive.");
} else if (num < 0) {
    console.log("Given number is negative.");
} else {
    console.log("Given number is zero.");
};
